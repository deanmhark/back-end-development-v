<h1>
     CHICKEN MENUDO
</h1>
<<div class="ing">
Chicken Menudo is a hearty Filipino stew made with pork,<br>
 liver, vegetables, and tomato sauce. Here's a basic recipe for Menudo:<br><br>
<div class="h11">Ingredients:</div><br>
-   Cooking oil<br>
-	1 Red onion, sliced<br>
-	6 pcs. Garlic Cloves, minced<br>
-	2 pcs. Potato, cubed<br>
-	1 pc. Carrot, cubed<br>
-	1/3 cup Dried Raisins<br>
-	6 pcs. Hotdog, sliced<br>
-	1 cup green peas <br>
-	1 pc. Chicken Cube<br>
-	2 cups of water<br>
-	1 teaspoon Ground Black Pepper<br>
-	2 tablespoons Fish Sauce or depends on your taste<br>
-	1 pc. Red bell pepper, sliced<br>
-	Tomato Sauce 250g<br>
-	750g to 1kilo Chicken, cut into pieces<br><br>

<div class="h22">Procedure: </div><br>
    <br>

    1.	Heat Cooking oil in a cooking pot<br><br>
2.	Once the cooking oi hot, add sliced Onion Sauté’ Onion until it becomes translucent<br><br>
3.	Then add minced Garlic Sauté until the garlic is fragrant <br><br>
4.	Add chicken sauté until there is no more pink parts in the chicken<br><br>
-	Once pink part is gone, cover the pot for 5 minutes for the chicken to be rendered<br><br>
5.	Then add these following ingredients:<br><br>
-	Add Ground black pepper<br>
-	Fish sauce<br>
-	Water<br>
-	Potato<br>
-	Carrot<br>
-	Red bell pepper<br>
-	Green peas<br>
-	Hotdog<br>
-	Dried raisins<br>
-	Stir it well<br>
6.	Cover the pot and simmer for 10 minutes, After 10 minutes add the chicken cube and tomato sauce, then stir it<br><br>
7.	After 5 minutes, your Chicken Menudo is ready <br><br>
8.	Menudo is best served hot and is commonly paired with rice.<br><br>

Menudo can be personalized by adding ingredients like raisins, chickpeas, or hotdogs, depending on personal preference. <br>
This is a basic recipe, and variations exist across different regions in the Philippines. <br>
Feel free to adjust the ingredients and seasoning to suit your taste.<br>

