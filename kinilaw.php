<h1>
     KINILAW NA TUNA
</h1>
<<div class="ing">
Kinilaw is a traditional Filipino dish that features raw seafood, <br>
typically fish or seafood marinated in vinegar or citrus juice. <br>
 Here's a basic recipe for Kinilaw: <br> <br>

<div class="h11">Ingredients:</div>
Ingredients: <br>
-	400 grams Tuna <br>
-	1 cup vinegar <br>
Marinade: <br>
-	1/2 cup vinegar  <br>
-	1/2 lime juice (3 tsp)  <br>
-	1 thumd size ginger  <br>
-	1 medium onion  <br>
-	4 pcs thai chili  <br>
-	1/2 tsp ground pepper  <br>
-	1 tsp salt <br> <br>

<div class="h22">Procedure: </div> 
    <br>
1.	1.	Cut the tuna into cubes, but pat it dry 1st <br>  <br>
2.	Put it into a bowl then wash it with 1 cup of vinegar, then drain it later <br>  <br>
3.	For marinade prepare and mince thumb size ginger, medium onion,  <br>  <br>
slice thinly the thai chili, cut the lime juice in half <br>   <br>
4.	After that mix it all and with a ½ cup of vinegar, ½ tsp ground pepper, 1 tsp salt <br>  <br>
5.	After done mixing add the tuna to marinate <br>  <br>
6.	Cover the marinate tuna and chill it for 1 hour <br>  <br>
7.	After 1 hour the kinilaws ready to serve <br> <br>  

Kinilaw can be customized based on personal preferences.  <br>
Some variations include adding tomatoes,  <br>
bell peppers, or even coconut milk for different flavors and textures.  <br>
Adjust the level of spiciness and acidity to suit your taste. <br>
