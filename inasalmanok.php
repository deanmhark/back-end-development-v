<h1>
    INASAL NA MANOK
</h1>
<<div class="ing">
Inasal na Manok is a popular Filipino grilled chicken dish known for its distinct flavor <br>
that comes from marinating the chicken in a mixture of vinegar, <br>
calamansi or lime juice, and various spices. Here's a basic recipe for Inasal na Manok:<br><br>
<div class="h11">Ingredients:</div>
-
- 1 whole chicken, cut into serving pieces (or chicken leg quarters)<br>
- 1/2 cup vinegar<br>
- 1/4 cup calamansi or lime juice<br>
- 4 cloves garlic, minced<br>
- 1 thumb-sized ginger, grated<br>
- 1 stalk lemongrass, pounded and chopped (optional)<br>
- 2 tablespoons brown sugar<br>
- 1 tablespoon fish sauce (patis)<br>
- 1 teaspoon ground black pepper<br>
- 1 teaspoon annatto powder (achuete) or annatto oil (atsuete) for color (optional)<br>
- Salt to taste<br>
- Bamboo skewers (for grilling)<br>
- Cooking oil for basting<br><br>

<div class="h22">Procedure: </div>
    <br>
1.
1. **Marinating the Chicken**:<br><br>
   - In a bowl, combine vinegar, calamansi or lime juice, minced garlic,<br><br> 
   grated ginger, lemongrass (if using), brown sugar, fish sauce, ground black pepper,<br><br>
    annatto powder or annatto oil (if using), and salt to taste. Mix well to create the marinade.<br><br>
   - Place the chicken pieces in the marinade, making sure they are fully coated. Marinate the chicken in the refrigerator for at least 2 to 4 hours, <br><br>
   or ideally overnight, to let the flavors infuse into the meat.<br><br>

2. **Preparing the Grill**:<br><br>
   - Preheat your grill to medium-high heat. If using charcoal, wait until the coals are hot and have turned ashy gray.<br><br>

3. **Skewering the Chicken**:<br><br>
   - Thread the marinated chicken pieces onto bamboo skewers,<br><br>
    starting from the thighs or drumsticks and working your way towards the top of the skewers.<br><br>
     This helps the chicken cook evenly and makes it easier to handle on the grill.<br><br>

4. **Grilling the Chicken**:<br><br>
   - Place the chicken skewers on the preheated grill. Cook the chicken, turning occasionally, <br><br>
   and baste it with cooking oil or the remaining marinade mixture to keep it moist and add flavor.<br><br>
   - Grill the chicken until it's cooked through and has a nicely charred and slightly caramelized exterior.<br><br>
    It usually takes about 20-30 minutes, depending on the thickness of the chicken pieces.<br><br>

5. **Serve**:<br><br>
   - Once the Inasal na Manok is fully cooked and has a beautiful grilled color, remove it from the grill.<br><br>
   - Serve the chicken hot, traditionally with a side of steamed rice and dipping sauces like sinamak (spiced vinegar) or soy sauce with calamansi or lime juice.<br><br>

Inasal na Manok is often enjoyed with achara (pickled green papaya) or sliced tomatoes and onions as a refreshing side dish.<br>
 Adjust the marinade ingredients to suit your taste preferences, and enjoy this flavorful Filipino grilled chicken dish!<br>

