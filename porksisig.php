<h1>
     PORK SISIG
</h1>
<<div class="ing">
Sisig is a popular Filipino dish made from chopped and seasoned parts of pig's head and liver,
<br> typically served on a sizzling plate. Here's a recipe for Sisig Pork: <br><br>
<div class="h11">Ingredients:</div>
- Ingredients:
-	1.5kg pork<br>
-	1 liter water<br>
-	3 stalks lemon grass<br>
-	1 head garlic<br>
-	1 medium onion<br>
-	1 tablespoon black pepper<br>
-	3 pcs laurel leaves<br>
-	3 tablespoons soy sauce<br>
-	green chili pepper<br>
-	calamansi juice(optional)<br> <br>

<div class="h22">Procedure: </div>
    <br>

 1. **Boiling and Grilling the Pork**:  <br>
   - Boil the pork belly (or pork cheeks) and pork liver until they are tender. Drain and pat dry.  <br>
   - Grill the boiled pork belly and liver until they are slightly charred and crispy on the outside. Alternatively,<br>
   <br> you can broil them in the oven for a similar effect. Allow them to cool, then chop finely.  <br> Some people prefer a finer chop for sisig,<br>
   while others prefer it slightly coarser.<br><br>

2. **Preparing the Sisig**:  <br>
   - In a hot pan or skillet, melt the butter or heat the cooking oil.  <br>
   - Saut√© the minced onion and garlic until fragrant and the onions turn translucent.<br>
   - Add the chopped grilled pork belly and liver to the pan. Stir and cook for a few minutes.<br><br>

3. **Adding Flavor**:<br>
   - Pour in the soy sauce and calamansi juice(optional) (or lemon juice) over the pork mixture. Stir well to combine.<br>
   - Add the chopped green chili peppers. Cook for a few more minutes to let the flavors meld together.<br><br>

4. **Optional Egg** (Sisig with Egg): <br>
   - If desired, create a space in the middle of the pan by pushing the pork mixture to the sides.<br>Crack an egg into the center of the pan.<br>
    Allow it to cook slightly, then scramble it and mix it with the rest of the sisig.<br><br>

5. **Seasoning**:
   - Season the sisig with salt and pepper according to your taste.<br>Adjust the seasoning as needed.<br><br>

6. **Serving**:  <br>
   - Transfer the Sisig Pork to a sizzling plate if available, or serve it on a regular plate.<br>
   - Garnish with freshly chopped spring onions on top for added freshness and flavor.<br><br>
Sisig is commonly served as an appetizer or as a main dish,   <br>often accompanied by steamed rice and sometimes with a side of calamansi or lemon wedges for added tanginess.

