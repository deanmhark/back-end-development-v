<h1>
     TORTANG TALONG
</h1>
<<div class="ing">
Torta Talong, also known as Eggplant Omelette or Eggplant Fritters,  <br>
 is a simple yet delicious Filipino dish made with grilled or roasted eggplants and eggs.  <br>
  Here's a basic recipe to make Torta Talong:  <br>  <br>
<div class="h11">Ingredients:</div>
- 
Ingredients:  <br>

- 2-3 large eggplants (Asian variety, long and slender)  <br>
- 3-4 eggs  <br>
- 1 small onion, finely chopped  <br>
- 2 cloves garlic, minced  <br>
- Salt and pepper to taste  <br>
- Cooking oil for frying
<br>  <br>
<div class="h22">Procedure: </div>
    <br>
1. **Preparing the Eggplants**:  <br>
   - Wash the eggplants and pat them dry. Prick the eggplants using a fork or knife in several places to prevent them from bursting while roasting.  <br>
   - Roast the eggplants directly over an open flame (gas stove or grill) until the skin is charred and the flesh becomes soft.   <br>
   Alternatively, you can bake them in the oven at 400°F (200°C) for about 30-40 minutes or until soft. Let the roasted eggplants cool.  <br>  <br>

2. **Peeling the Eggplants**:  <br>
   - Once the roasted eggplants have cooled down, peel off the charred skin.   <br>
   Leave the stems intact and gently flatten the eggplants by pressing down with a fork or using the back of a knife.  <br>  <br>

3. **Making the Egg Mixture**:  <br>
   - In a bowl, beat the eggs. Add the finely chopped onion, minced garlic, salt, and pepper. Mix everything together well.  <br>  <br>

4. **Assembling and Frying**:  <br>
   - Heat some cooking oil in a non-stick skillet or frying pan over medium heat.  <br>
   - Dip each flattened eggplant into the egg mixture, ensuring they are well coated on both sides.  <br>
   - Carefully place the eggplant slices into the hot skillet and cook until the bottom side is golden brown.   <br>
   Flip them over and cook the other side until it's golden brown and the egg is cooked through.  <br>  <br>

5. **Repeat and Serve**:  <br>
   - Repeat the process with the remaining eggplant slices, adding more oil to the pan as needed.  <br>
   - Once all the eggplant slices are cooked and golden brown, transfer them to a serving plate.
   <br>  <br>
6. **Serve**:  <br>
   - Torta Talong is typically served warm and can be enjoyed with rice or as a side dish with a dipping sauce like vinegar with garlic and chili.  <br>  <br>

   
Variations of Torta Talong may include adding ground pork or shrimp to the egg mixture for added flavor.  <br>
 Adjust the seasoning and ingredients to suit your taste preferences.  <br>

