<h1>
     Adobong Manok
</h1>
<<div class="ing">
     is a popular Filipino dish made with chicken marinated and cooked in vinegar, 
     <br>soy sauce, garlic, and other seasonings. Here's a typical recipe for Adobo Manok:
<div class="h11">Ingredients:</div>
- ½ kilo chicken<br>
- 65ml or 1/4 cup soy sauce<br>
- 35ml or 1/8 cup white vinegar <br>
- 1 whole garlic, chopped<br>
- 2 pcs bay leaves<br>
- 1 teaspoon whole black peppercorns<br>
- 600ml or 2.5 cup water<br>
- 3 tablespoon Cooking oil<br>
- Cayenne Pepper 2 to 4 pcs<br><br><br>
<div class="h22">Procedure: </div>
    <br>
1.	Heat Oil in Cooking pot and saute’ the garlic
2.	After sauteing the garlic, add the chicken <br>
-	Stir well so that the flavor of the garlic will be absorbed by the chicken<br>
3.	Cover the pot for 2 minutes<br>
-	After 2 minutes, check the chicken if there are pink parts left<br>
4.	Then add these ingredients:<br>
-	Add the soy sauce<br>
-	Add the Vinegar<br>
-	Add water<br>
-	Add the peppercorn<br>
-	Add the bey leaves <br>
-	Add the cayenne pepper so that our Adobo will be a little bit spicy.<br> (This is an optional ingredient, meaning it can be added or not, depending on your taste)<br>
5.	Cover the pot, and let it boil for 30 minutes<br>
6.	After 30 minutes, check if the sauce is already thick<br>
-	If not, heat the pot again until it becomes thick<br>
-	 Once the chicken is tender and the sauce has thickened to your liking, remove the pot from heat.<br>
-	 Serve the Adobo Manok hot with steamed rice.<br><br><br>
Adobo Manok is typically served with rice, <br>and the flavorful sauce is often drizzled over the rice. <br>Some variations of this recipe might include additional ingredients like potatoes <br>or hard-boiled eggs added during the simmering process. <br>Feel free to adjust the recipe to suit your taste preferences.
