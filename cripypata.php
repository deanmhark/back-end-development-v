<h1>
     CRISPY PATA
</h1>
<<div class="ing">
Crispy Pata is a famous Filipino dish known for its deep-fried pork   <br>
knuckle or pork leg with crispy skin and tender meat inside.   <br>
Here's a basic recipe to make Crispy Pata:  <br>  <br>


<div class="h11">Ingredients:</div>
- - 1 whole pork leg (pata), preferably front leg, cleaned and prepared by the butcher  <br>
- 6 cloves garlic, crushed  <br>
- 4 pieces dried bay leaves  <br>
- 1 tablespoon whole peppercorns  <br>
- 1 tablespoon salt  <br>
- Water for boiling  <br>
- Cooking oil for deep-frying  <br>  <br>

<div class="h22">Procedure: </div>
    <br>
1.
1. **Prepping the Pork Leg**:  <br>  <br>
   - In a large pot, place the pork leg and cover it with enough water. Add crushed garlic, dried bay leaves, whole peppercorns, and salt.  <br>  <br>
   - Bring the pot to a boil over high heat. Once boiling, lower the heat to a simmer. Let it cook until the pork leg is tender,  <br>  <br>
    which can take about 1.5 to 2 hours depending on the size and tenderness of the pork. Check occasionally and add more water if needed.  <br>  <br>

2. **Draining and Cooling**:  <br>  <br>
   - Once the pork leg is tender, carefully remove it from the pot and drain excess liquid.   <br>  <br>
   Let it cool for a while. Some prefer refrigerating it for a few hours or overnight to ensure the skin dries out.  <br>  <br>

3. **Deep-Frying**:  <br>  <br>
   - Heat cooking oil in a deep frying pan or pot to a temperature of around 350-375°F (175-190°C).  <br>  <br>
   - Carefully and slowly lower the pork leg into the hot oil. Be cautious as it might cause splattering.  <br>  <br>
   - Fry the pork leg until the skin turns golden brown and crispy. Ensure the meat is thoroughly cooked inside,   <br>  <br>
   which usually takes about 20-30 minutes.  <br>  <br>

4. **Draining and Resting**:  <br>  <br>
   - Once the Crispy Pata is cooked and crispy, remove it from the oil and let it drain on a wire rack or paper towels to remove excess oil.  <br>  <br>

5. **Serving**:  <br>  <br>
   - Serve the Crispy Pata hot and crispy, traditionally with a dipping sauce made of vinegar, soy sauce,   <br>  <br>
   chopped onions, and chili peppers.  <br>  <br>

Crispy Pata is typically served as a main dish and is often shared among several people due to its large portion size.  <br>  <br>
 It's best paired with steamed rice and enjoyed with family or friends during special occasions or gatherings.   <br>  <br>
 Adjust the seasoning and cooking time based on your preferences for a perfect Crispy Pata experiences.  <br>  <br>
