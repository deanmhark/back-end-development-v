<h1>
     SINIGANG NA MANOK
</h1>
<<div class="ing">
Sinigang Manok is a Filipino sour soup made with chicken, vegetables, and a souring agent, <br>
often tamarind or other sour fruits. Here's a recipe to make Sinigang Manok:<br><br>
<div class="h11">Ingredients:</div><br>

-	Cooking oil <br>
-	Red Onion 1 pc large size, chopped <br>
-	Tomato 4 pcs, medium size, chopped <br>
-	Chicken 750g to 1 kilo <br>
-	Water 2 liters <br>
-	Ground black pepper 1 teaspoon <br>
-	Fish sauce 2 tablespoons or adjust to taste <br>
-	Tamarind powder mix 2 packs of 22g <br>
-	Okra  6 pcs, chopped <br>
-	Water spinach 1 to 2 bowls <br>
-	Finger pepper 4 pcs, chopped ( optional only ) <br>
-	Taro 2 pcs, cubed ( optional only )
<br><br>
<div class="h22">Procedure: </div>
    <br>

1.	Heat cooking oil in a cooking pot, Once its hot, add the chopped Onion<br><br>
2.	Add Tomatoes sauté for 1 minute to give the soup more vibrant color, making it more appetizing<br><br>
3.	After sauteing the onion and tomatoes, add the chicken then sauté for 30 seconds, after sauteing, cover the pot for 2 minutes<br><br>
4.	After 2 minutes, that’s were you add 2 liters of water stir it then add 1 teaspoon of ground black pepper<br><br>
5.	Add 2 tablespoons of Fish sauce<br><br>
6.	Add the chopped Finger pepper(optional)<br><br>
7.	Cover the pot for 15 minutes, after 15 minutes Add the Add 2 packs of Tamarind powder mix, you may add taro so the soup to be a little thicker, stir it to mix it well.<br><br>
8.	Cover the pot 10 minutes, after 10 minutes add the vegetables: Okra, Water spinach<br><br>
-	Submerge the vegetables on the soup<br><br>
-	Set the heat  to low, then cover the poi for 5 minutes<br><br>
-	After 5 minutes it should be ready<br><br>
-	 Sinigang Manok is best served hot, usually with steamed rice on the side.<br><br>

Sinigang can be made using other souring agents such as guava, <br>
calamansi, or green mango. Adjust the ingredients and sourness level according to your taste preferences.<br>
